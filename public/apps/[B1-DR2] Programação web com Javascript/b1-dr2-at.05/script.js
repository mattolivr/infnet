const primeiroNumero = Number(prompt("Digite o primeiro valor:"));
const segundoNumero = Number(prompt("Digite o segundo valor:"));

const soma = primeiroNumero + segundoNumero;
const subtracao = primeiroNumero - segundoNumero;
const multiplicacao = primeiroNumero * segundoNumero;
const divisao = primeiroNumero / segundoNumero;

alert(
  `Soma: ${soma}
  Subtração: ${subtracao}
  Multiplicação: ${multiplicacao}
  Divisão: ${divisao}`
);
