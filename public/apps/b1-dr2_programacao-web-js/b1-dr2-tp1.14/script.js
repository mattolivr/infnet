let massa = Number(prompt("Informe o valor da massa (kg): "));
let aceleracao = Number(prompt("Informe o valor da aceleração (m/s²): "));

alert("Força: " + (massa * aceleracao) + " Newtons")

let trabalho = Number(prompt("Informe o valor do trabalho (J): "));
let tempo = Number(prompt("Informe o tempo decorrido (s): "));

alert("Potência: " + (trabalho / tempo) + " Watts");